#include "3-calc.h"

/**
 * op_add - adds.
 * @a: x.
 * @b: y.
 * Return: @a + @b.
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - Returns @a - @b.
 * @a: x.
 * @b: y.
 * Return: @a - @b.
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplicates.
 * @a: x.
 * @b: y.
 * Return: @a * @b.
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides.
 * @a: x.
 * @b: y.
 * Return: @a / @b.
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - Returns the remainder of a division.
 * @a: x.
 * @b: y.
 * Return: @a % @b.
 */
int op_mod(int a, int b)
{
	return (a % b);
}
